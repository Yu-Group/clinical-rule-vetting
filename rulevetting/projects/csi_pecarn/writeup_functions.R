plotRelFreq <- function(data, high = TRUE) {
  # Function to plot relative frequency bar graphs in the writeup.
  # Args:
  #  data: dataset
  #  high: logical argument determining whether to use the higher differences
  #    or lower differences.
  # Returns: side by side bar graph of relative frequencies.
  library(tidyverse)
  if (high) {
    d <- head(data, 5)
  } else {
    d <- tail(data, 5)
  }
  d %>% select(-diff) %>% melt() %>% 
    ggplot(mapping = aes(x = feature, y = value, fill = variable)) + 
    geom_col(position = 'dodge') + 
    labs(x = "Feature", y = "Relative Frequency") +
    scale_fill_manual("Value", values = c("mediumpurple3", "mediumseagreen")) + 
    theme_classic() + 
    theme(axis.text.x = element_text(angle = 45, hjust = 1))
}



plotROC <- function(sens, spec, model = "Decision Tree") {
  # Function to plot ROC curves.
  # Args:
  #  sens: sensitivity vector
  #  spec: specificity vector
  #  model: type of model to plot
  # Returns: ROC curve plot
  library(ggplot2)
  col <- "firebrick2"
  df <- data.frame(sens, spec)
  p <- df %>% ggplot(mapping = aes(sens, spec)) + 
    geom_point(size = 1, color = col) + 
    geom_path(color = col) +
    labs(x = "Sensitivity", y = "Specificity", title = model) + 
    geom_point(aes(x = 0.9439, y = 0.3903), size = 3, color = "black") +
    theme_minimal() + 
    theme(plot.title = element_text(hjust = 0.5))
  return(p)
}


